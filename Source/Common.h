class AutoDeleteFile
{
private:
	std::string filename_;
public:
	explicit AutoDeleteFile(const std::string& filename) : filename_(filename) { }
	~AutoDeleteFile() { if ( ! filename_.empty()) unlink(filename_.c_str()); }
	void CancelDelete() { filename_.clear(); }
};

